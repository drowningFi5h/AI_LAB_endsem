// Multiply operation in GF(2^8) for MixColumns
static unsigned char aes_gf_multiply(unsigned char a, unsigned char b) {
    unsigned char result = 0;
    unsigned char high_bit_set;

    for (int i = 0; i < 8; i++) {
        if (b & 1) {
            result ^= a;
        }

        high_bit_set = a & 0x80;
        a <<= 1;

        if (high_bit_set) {
            a ^= 0x1B;  // AES irreducible polynomial: x^8 + x^4 + x^3 + x + 1
        }

        b >>= 1;
    }

    return result;
}

// Substitute each byte of the matrix using the S-Box
static void aes_sub_bytes(unsigned char state[4][4], const unsigned char sbox[256]) {
    for (int i = 0; i < 4; i++) {
        for (int j = 0; j < 4; j++) {
            state[i][j] = sbox[state[i][j]];
        }
    }
}

// Shift the rows of the matrix
static void aes_shift_rows(unsigned char state[4][4]) {
    unsigned char temp;

    // Shift second row by 1
    temp = state[1][0];
    state[1][0] = state[1][1];
    state[1][1] = state[1][2];
    state[1][2] = state[1][3];
    state[1][3] = temp;
}

// MixColumns operation
static void aes_mix_columns(unsigned char state[4][4]) {
    unsigned char temp[4];

    for (int i = 0; i < 4; i++) {
        temp[0] = aes_gf_multiply(0x01, state[0][i]) ^ aes_gf_multiply(0x04, state[1][i]) ^ aes_gf_multiply(0x04, state[2][i]) ^ aes_gf_multiply(0x05, state[3][i]);
        temp[1] = aes_gf_multiply(0x05, state[0][i]) ^ aes_gf_multiply(0x01, state[1][i]) ^ aes_gf_multiply(0x04, state[2][i]) ^ aes_gf_multiply(0x04, state[3][i]);
        temp[2] = aes_gf_multiply(0x04, state[0][i]) ^ aes_gf_multiply(0x05, state[1][i]) ^ aes_gf_multiply(0x01, state[2][i]) ^ aes_gf_multiply(0x04, state[3][i]);
        temp[3] = aes_gf_multiply(0x04, state[0][i]) ^ aes_gf_multiply(0x04, state[1][i]) ^ aes_gf_multiply(0x05, state[2][i]) ^ aes_gf_multiply(0x01, state[3][i]);

        for (int j = 0; j < 4; j++) {
            state[j][i] = temp[j];
        }
    }
}

static void aes_mix_columns_inverse(unsigned char state[4][4]) {
    unsigned char temp[4];

    for (int i = 0; i < 4; i++) {
        temp[0] = aes_gf_multiply(165, state[0][i]) ^ aes_gf_multiply(7, state[1][i]) ^ aes_gf_multiply(26, state[2][i]) ^ aes_gf_multiply(115, state[3][i]);
        temp[1] = aes_gf_multiply(115, state[0][i]) ^ aes_gf_multiply(165, state[1][i]) ^ aes_gf_multiply(7, state[2][i]) ^ aes_gf_multiply(26, state[3][i]);
        temp[2] = aes_gf_multiply(26, state[0][i]) ^ aes_gf_multiply(115, state[1][i]) ^ aes_gf_multiply(165, state[2][i]) ^ aes_gf_multiply(7, state[3][i]);
        temp[3] = aes_gf_multiply(7, state[0][i]) ^ aes_gf_multiply(26, state[1][i]) ^ aes_gf_multiply(115, state[2][i]) ^ aes_gf_multiply(165, state[3][i]);

        for (int j = 0; j < 4; j++) {
            state[j][i] = temp[j];
        }
    }
}

void aes_key_expansion(unsigned char key[16], unsigned char round_keys[176]) {
    const int Nb = 4; // Number of columns in the state (always 4 for AES)
    const int Nk = 4; // Number of 32-bit words in the key (128-bit key -> 4 words)
    const int Nr = 10; // Number of rounds

    unsigned char temp[4];
    int i, j;

    // The first round key is the original key
    for (i = 0; i < Nk * Nb; i++) {
        round_keys[i] = key[i];
    }

    // Main key expansion loop
    for (i = Nk; i < Nb * (Nr + 1); i++) {
        for (j = 0; j < 4; j++) {
            temp[j] = round_keys[(i - 1) * 4 + j];
        }

        if (i % Nk == 0) {
            // RotWord
            {
                unsigned char t = temp[0];
                temp[0] = temp[1];
                temp[1] = temp[2];
                temp[2] = temp[3];
                temp[3] = t;
            }

            // SubWord
            {
                temp[0] = k_sBox[temp[0]];
                temp[1] = k_sBox[temp[1]];
                temp[2] = k_sBox[temp[2]];
                temp[3] = k_sBox[temp[3]];
            }

            temp[0] ^= Rcon[i / Nk]; // XOR with the round constant
        } else if (Nk > 6 && i % Nk == 4) {
            // SubWord
            {
                temp[0] = k_sBox[temp[0]];
                temp[1] = k_sBox[temp[1]];
                temp[2] = k_sBox[temp[2]];
                temp[3] = k_sBox[temp[3]];
            }
        }

        // XOR temp with the (i-Nk)th word
        round_keys[i * 4 + 0] = round_keys[(i - Nk) * 4 + 0] ^ temp[0];
        round_keys[i * 4 + 1] = round_keys[(i - Nk) * 4 + 1] ^ temp[1];
        round_keys[i * 4 + 2] = round_keys[(i - Nk) * 4 + 2] ^ temp[2];
        round_keys[i * 4 + 3] = round_keys[(i - Nk) * 4 + 3] ^ temp[3];
    }
}

unsigned char round_keys[11][4][4];

// Main AES encryption function (one round)
void aes_one_round_encrypt(unsigned char plaintext[16], unsigned char key[16], unsigned char ciphertext[16]) {
    unsigned char state[4][4];

    // XOR plaintext and key to create the initial state
    for (int i = 0; i < 16; i++) {
        state[i % 4][i / 4] = plaintext[i] ^ key[i];
    }

    // SubBytes
    aes_sub_bytes(state, sBox);

    // ShiftRows
    aes_shift_rows(state);

    // MixColumns
    aes_mix_columns(state);

    // Copy the final state to the ciphertext
    for (int i = 0; i < 16; i++) {
        ciphertext[i] = state[i % 4][i / 4];
    }
}

// Main AES decryption function (one round)
void aes_one_round_decrypt(unsigned char ciphertext[16], unsigned char key[16], unsigned char plaintext[16]) {
    unsigned char state[4][4];

    // Copy ciphertext to the state
    for (int i = 0; i < 16; i++) {
        state[i % 4][i / 4] = ciphertext[i];
    }

    // Inverse MixColumns
    aes_mix_columns_inverse(state);

    // Inverse ShiftRows
    // (Note: Inverse of ShiftRows is the same operation as ShiftRows for one round)
    aes_shift_rows(state);

    // Inverse SubBytes
    aes_sub_bytes(state, inv_sBox);

    // XOR state with key to get plaintext
    for (int i = 0; i < 16; i++) {
        plaintext[i] = state[i % 4][i / 4] ^ key[i];
    }
}

// Main AES encryption function (10 rounds)
void aes_encrypt_10_rounds(unsigned char plaintext[16], unsigned char key[16], unsigned char ciphertext[16]) {
    unsigned char state[4][4];

    // XOR plaintext and key to create the initial state
    for (int i = 0; i < 16; i++) {
        state[i % 4][i / 4] = plaintext[i] ^ key[i];
    }

    // Perform 9 rounds of AES encryption operations
    for (int round = 0; round < 9; round++) {
        aes_sub_bytes(state, sBox);
        aes_shift_rows(state);
        aes_mix_columns(state);
        aes_key_expansion(key, round_keys); // Update round keys
    }

    // Perform the 10th round without MixColumns
    aes_sub_bytes(state, sBox);
    aes_shift_rows(state);
    aes_key_expansion(key, round_keys); // Update round keys

    // Copy the final state to the ciphertext
    for (int i = 0; i < 16; i++) {
        ciphertext[i] = state[i % 4][i / 4];
    }
}

// Main AES decryption function (10 rounds)
void aes_decrypt_10_rounds(unsigned char ciphertext[16], unsigned char key[16], unsigned char plaintext[16]) {
    unsigned char state[4][4];

    // Copy ciphertext to the state
    for (int i = 0; i < 16; i++) {
        state[i % 4][i / 4] = ciphertext[i];
    }

    // Perform 9 rounds of AES decryption operations
    for (int round = 0; round < 9; round++) {
        aes_shift_rows(state);
        aes_sub_bytes(state, inv_sBox);
        aes_key_expansion(key, round_keys); // Update round keys
        aes_mix_columns_inverse(state);
    }

    // Perform the 10th round without MixColumns
    aes_shift_rows(state);
    aes_sub_bytes(state, inv_sBox);
    aes_key_expansion(key, round_keys); // Update round keys

    // XOR state with key to get plaintext
    for (int i = 0; i < 16; i++) {
        plaintext[i] = state[i % 4][i / 4] ^ key[i];
    }
}


