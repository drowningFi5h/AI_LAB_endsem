// Galois Field multiplication in GF(2^8)
uint8_t gmul(uint8_t a, uint8_t b) {
    uint8_t p = 0;

    for (int i = 0; i < 8; i++) {
        if (b & 1) {
            p ^= a;
        }

        // Check if a would overflow
        uint8_t hi_bit_set = (a & 0x80);

        // Shift a left
        a <<= 1;

        // If high bit was set before shifting, XOR with irreducible polynomial
        if (hi_bit_set) {
            a ^= 0x1B;  // x^8 + x^4 + x^3 + x + 1 polynomial
        }

        // Shift b right
        b >>= 1;
    }

    return p;
}

// SubBytes transformation using S-box lookup
void subBytes(uint8_t *state) {
    for (int i = 0; i < AES_BLOCK_SIZE; i++) {
        state[i] = SBOX[state[i]];
    }
}

// Inverse SubBytes transformation
void invSubBytes(uint8_t *state) {
    for (int i = 0; i < AES_BLOCK_SIZE; i++) {
        state[i] = INV_SBOX[state[i]];
    }
}

// ShiftRows transformation
void shiftRows(uint8_t *state) {
    // Row 0 (first row) remains unchanged

    // Row 1: Rotate left by 1
    uint8_t temp = state[1];
    state[1] = state[5];
    state[5] = state[9];
    state[9] = state[13];
    state[13] = temp;

    // Row 2: Rotate left by 2
    temp = state[2];
    state[2] = state[10];
    state[10] = temp;
    temp = state[6];
    state[6] = state[14];
    state[14] = temp;

    // Row 3: Rotate left by 3
    temp = state[15];
    state[15] = state[11];
    state[11] = state[7];
    state[7] = state[3];
    state[3] = temp;
}

// Inverse ShiftRows transformation
void invShiftRows(uint8_t *state) {
    // Row 0 (first row) remains unchanged

    // Row 1: Rotate right by 1
    uint8_t temp = state[13];
    state[13] = state[9];
    state[9] = state[5];
    state[5] = state[1];
    state[1] = temp;

    // Row 2: Rotate right by 2
    temp = state[10];
    state[10] = state[2];
    state[2] = temp;
    temp = state[14];
    state[14] = state[6];
    state[6] = temp;

    // Row 3: Rotate right by 3
    temp = state[3];
    state[3] = state[7];
    state[7] = state[11];
    state[11] = state[15];
    state[15] = temp;
}

// MixColumns transformation
void mixColumns(uint8_t *state) {
    for (int col = 0; col < 4; col++) {
        uint8_t a[4];
        uint8_t b[4];

        // Copy column
        for (int i = 0; i < 4; i++) {
            a[i] = state[col * 4 + i];
            b[i] = (a[i] << 1) ^ ((a[i] & 0x80) ? 0x1B : 0x00);
        }

        // Matrix multiplication in Galois Field
        state[col * 4 + 0] = b[0] ^ a[3] ^ a[2] ^ b[1] ^ a[1];
        state[col * 4 + 1] = b[1] ^ a[0] ^ a[3] ^ b[2] ^ a[2];
        state[col * 4 + 2] = b[2] ^ a[1] ^ a[0] ^ b[3] ^ a[3];
        state[col * 4 + 3] = b[3] ^ a[2] ^ a[1] ^ b[0] ^ a[0];
    }
}

// Inverse MixColumns transformation
void invMixColumns(uint8_t *state) {
    for (int col = 0; col < 4; col++) {
        uint8_t a[4];

        // Copy column
        for (int i = 0; i < 4; i++) {
            a[i] = state[col * 4 + i];
        }

        // Inverse matrix multiplication in Galois Field
        state[col * 4 + 0] = gmul(a[0], 0x0E) ^ gmul(a[3], 0x0B) ^ gmul(a[2], 0x0D) ^ gmul(a[1], 0x09);
        state[col * 4 + 1] = gmul(a[1], 0x0E) ^ gmul(a[0], 0x0B) ^ gmul(a[3], 0x0D) ^ gmul(a[2], 0x09);
        state[col * 4 + 2] = gmul(a[2], 0x0E) ^ gmul(a[1], 0x0B) ^ gmul(a[0], 0x0D) ^ gmul(a[3], 0x09);
        state[col * 4 + 3] = gmul(a[3], 0x0E) ^ gmul(a[2], 0x0B) ^ gmul(a[1], 0x0D) ^ gmul(a[0], 0x09);
    }
}

// RotWord function
void rotWord(uint8_t *word) {
    uint8_t temp = word[0];
    word[0] = word[1];
    word[1] = word[2];
    word[2] = word[3];
    word[3] = temp;
}

// SubWord function
void subWord(uint8_t *word) {
    for (int i = 0; i < 4; i++) {
        word[i] = SBOX[word[i]];
    }
}

// Key Expansion function
void keyExpansion(const uint8_t *key, uint8_t *roundKeys) {
    uint8_t temp[4];
    uint8_t rcon_iter = 1;

    // Copy the original key
    memcpy(roundKeys, key, AES_KEY_SIZE);

    for (int i = Nk; i < Nb * (Nr + 1); i++) {
        memcpy(temp, roundKeys + (i - 1) * 4, 4);

        if (i % Nk == 0) {
            // RotWord
            rotWord(temp);

            // SubWord
            subWord(temp);

            // XOR with Rcon
            temp[0] ^= Rcon[rcon_iter];
            rcon_iter <<= 1;
            if ((rcon_iter & 0x80) != 0) {
                rcon_iter ^= 0x1B;
            }
        } else if (Nk > 6 && i % Nk == 4) {
            // SubWord
            subWord(temp);
        }

        for (int j = 0; j < 4; j++) {
            roundKeys[i * 4 + j] = roundKeys[(i - Nk) * 4 + j] ^ temp[j];
        }
    }
}

// AES-256 Encryption
void aesEncrypt(uint8_t *state, const uint8_t *key) {
    uint8_t roundKeys[AES_BLOCK_SIZE * (AES_ROUNDS + 1)];
    keyExpansion(key, roundKeys);

    // Initial round
    for (int i = 0; i < AES_BLOCK_SIZE; i++) {
        state[i] ^= roundKeys[i];
    }

    // Main rounds
    for (int round = 1; round < AES_ROUNDS; round++) {
        subBytes(state);
        shiftRows(state);
        mixColumns(state);

        // Add round key
        for (int i = 0; i < AES_BLOCK_SIZE; i++) {
            state[i] ^= roundKeys[round * AES_BLOCK_SIZE + i];
        }
    }

    // Final round
    subBytes(state);
    shiftRows(state);
    for (int i = 0; i < AES_BLOCK_SIZE; i++) {
        state[i] ^= roundKeys[AES_ROUNDS * AES_BLOCK_SIZE + i];
    }
}

// AES-256 Decryption
void aesDecrypt(uint8_t *state, const uint8_t *key) {
    uint8_t roundKeys[AES_BLOCK_SIZE * (AES_ROUNDS + 1)];
    keyExpansion(key, roundKeys);

    // Initial round
    for (int i = 0; i < AES_BLOCK_SIZE; i++) {
        state[i] ^= roundKeys[AES_ROUNDS * AES_BLOCK_SIZE + i];
    }

    // Inverse rounds
    for (int round = AES_ROUNDS - 1; round > 0; round--) {
        invShiftRows(state);
        invSubBytes(state);

        // Add round key
        for (int i = 0; i < AES_BLOCK_SIZE; i++) {
            state[i] ^= roundKeys[round * AES_BLOCK_SIZE + i];
        }

        invMixColumns(state);
    }

    // Final round
    invShiftRows(state);
    invSubBytes(state);
    for (int i = 0; i < AES_BLOCK_SIZE; i++) {
        state[i] ^= roundKeys[i];
    }
}

